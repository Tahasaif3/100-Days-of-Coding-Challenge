function useCompoundOperators() {
    var y = 22;
    console.log("Initial y:", y);
    y += 5;
    console.log("After Addition:", y);
    y -= 10;
    console.log("After Substraction:", y);
    y *= 6;
    console.log("After Muultiplication:", y);
    y /= 2;
    console.log("After Division:", y);
}
useCompoundOperators();
